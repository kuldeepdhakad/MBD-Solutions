import * as React from "react";
import { cn } from "@/lib/utils";

export const Input = React.forwardRef<
  HTMLInputElement,
  React.InputHTMLAttributes<HTMLInputElement>
>(({ className, type, ...props }, ref) => (
  <input
    type={type}
    className={cn(
      "flex h-11 w-full rounded-xl border border-border bg-surface px-4 text-sm text-primary outline-none transition placeholder:text-muted/70 focus:border-accent focus:ring-2 focus:ring-accent/15",
      className,
    )}
    ref={ref}
    {...props}
  />
));
Input.displayName = "Input";

